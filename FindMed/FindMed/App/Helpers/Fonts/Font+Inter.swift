import SwiftUI

extension Font {
    static func black(size: CGFloat) -> Font {
        Font.custom(Fonts.Inter.Black, size: size)
    }
    
    static func blackItalic(size: CGFloat) -> Font {
        Font.custom(Fonts.Inter.BlackItalic, size: size)
    }
    
    static func bold(size: CGFloat) -> Font {
        Font.custom(Fonts.Inter.Bold, size: size)
    }
    
    static func boldItalic(size: CGFloat) -> Font {
        Font.custom(Fonts.Inter.BoldItalic, size: size)
    }
    
    static func extraBold(size: CGFloat) -> Font {
        Font.custom(Fonts.Inter.ExtraBold, size: size)
    }
    
    static func extraBoldItalic(size: CGFloat) -> Font {
        Font.custom(Fonts.Inter.ExtraBoldItalic, size: size)
    }
    
    static func italic(size: CGFloat) -> Font {
        Font.custom(Fonts.Inter.Italic, size: size)
    }
    
    static func medium(size: CGFloat) -> Font {
        Font.custom(Fonts.Inter.Medium, size: size)
    }
    
    static func mediumItalic(size: CGFloat) -> Font {
        Font.custom(Fonts.Inter.MediumItalic, size: size)
    }
    
    static func regular(size: CGFloat) -> Font {
        Font.custom(Fonts.Inter.Regular, size: size)
    }
    
    static func semiBold(size: CGFloat) -> Font {
        Font.custom(Fonts.Inter.SemiBold, size: size)
    }
    
    static func semiBoldItalic(size: CGFloat) -> Font {
        Font.custom(Fonts.Inter.SemiBoldItalic, size: size)
    }
}
